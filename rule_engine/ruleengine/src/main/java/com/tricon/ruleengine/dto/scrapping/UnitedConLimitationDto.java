package com.tricon.ruleengine.dto.scrapping;

public class UnitedConLimitationDto {
	
	private int copay;
	private String limitation;
	private String appliedtoDed;
	
	public int getCopay() {
		return copay;
	}
	public void setCopay(int copay) {
		this.copay = copay;
	}
	public String getLimitation() {
		return limitation;
	}
	public void setLimitation(String limitation) {
		this.limitation = limitation;
	}
	public String getAppliedtoDed() {
		return appliedtoDed;
	}
	public void setAppliedtoDed(String appliedtoDed) {
		this.appliedtoDed = appliedtoDed;
	}
	
	
	

}
