package com.tricon.ruleengine.dto;

public class Rule6Dto {
	
	
	String ivfName;
	String percentage;
	String fsName;
	
	
	
	public Rule6Dto(String ivfName, String percentage, String fsName) {
		super();
		this.ivfName = ivfName;
		this.percentage = percentage;
		this.fsName = fsName;
	}
	public String getIvfName() {
		return ivfName;
	}
	public void setIvfName(String ivfName) {
		this.ivfName = ivfName;
	}
	public String getPercentage() {
		return percentage;
	}
	public void setPercentage(String percentage) {
		this.percentage = percentage;
	}
	public String getFsName() {
		return fsName;
	}
	public void setFsName(String fsName) {
		this.fsName = fsName;
	}
	
	
	


	
	

}
