package com.tricon.ruleengine.model.sheet;

public class InsuranceDetail {
	
	private String patientId;
	private String employerName;
	private String insuranceName;
	private String insuranceAddress;
	private String insuranceCity;
	private String insuranceState;
	private String insuranceZipCode;
	private String insurancePhone;
	public String getPatientId() {
		return patientId;
	}
	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}
	public String getEmployerName() {
		return employerName;
	}
	public void setEmployerName(String employerName) {
		this.employerName = employerName;
	}
	public String getInsuranceName() {
		return insuranceName;
	}
	public void setInsuranceName(String insuranceName) {
		this.insuranceName = insuranceName;
	}
	public String getInsuranceAddress() {
		return insuranceAddress;
	}
	public void setInsuranceAddress(String insuranceAddress) {
		this.insuranceAddress = insuranceAddress;
	}
	public String getInsuranceCity() {
		return insuranceCity;
	}
	public void setInsuranceCity(String insuranceCity) {
		this.insuranceCity = insuranceCity;
	}
	
	
	public String getInsuranceState() {
		return insuranceState;
	}
	public void setInsuranceState(String insuranceState) {
		this.insuranceState = insuranceState;
	}
	public String getInsuranceZipCode() {
		return insuranceZipCode;
	}
	public void setInsuranceZipCode(String insuranceZipCode) {
		this.insuranceZipCode = insuranceZipCode;
	}
	public String getInsurancePhone() {
		return insurancePhone;
	}
	public void setInsurancePhone(String insurancePhone) {
		this.insurancePhone = insurancePhone;
	}
	
	
	
	
	
	

}
