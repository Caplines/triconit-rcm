package com.tricon.ruleengine.model.sheet;

public class PatientPolicyHolder {

	String policyHolder;
	String patientId;
	String policyHolderSec;
	String relation;
	

	public String getPolicyHolder() {
		return policyHolder;
	}

	public void setPolicyHolder(String policyHolder) {
		this.policyHolder = policyHolder;
	}

	public String getPatientId() {
		return patientId;
	}

	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}

	public String getPolicyHolderSec() {
		return policyHolderSec;
	}

	public void setPolicyHolderSec(String policyHolderSec) {
		this.policyHolderSec = policyHolderSec;
	}

	public String getRelation() {
		return relation;
	}

	public void setRelation(String relation) {
		this.relation = relation;
	}
	
	
	
	
	
}
