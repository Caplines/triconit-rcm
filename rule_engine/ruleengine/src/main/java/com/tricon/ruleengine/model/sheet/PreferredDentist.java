package com.tricon.ruleengine.model.sheet;

public class PreferredDentist {

	private String patientId;
	private String patientName;
	private String preferredDentist;
	private String providerName;
	public String getPatientId() {
		return patientId;
	}
	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}
	public String getPatientName() {
		return patientName;
	}
	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	public String getPreferredDentist() {
		return preferredDentist;
	}
	public void setPreferredDentist(String preferredDentist) {
		this.preferredDentist = preferredDentist;
	}
	public String getProviderName() {
		return providerName;
	}
	public void setProviderName(String providerName) {
		this.providerName = providerName;
	}
	
	
}
